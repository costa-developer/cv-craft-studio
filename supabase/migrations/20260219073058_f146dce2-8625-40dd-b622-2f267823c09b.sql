
-- Create storage bucket for CV photos
INSERT INTO storage.buckets (id, name, public) VALUES ('cv-photos', 'cv-photos', true)
ON CONFLICT (id) DO NOTHING;

-- Allow authenticated users to upload their own photos
CREATE POLICY "Users can upload their own photos" ON storage.objects
FOR INSERT WITH CHECK (
  bucket_id = 'cv-photos' AND auth.uid()::text = (storage.foldername(name))[1]
);

-- Allow public read access
CREATE POLICY "CV photos are publicly accessible" ON storage.objects
FOR SELECT USING (bucket_id = 'cv-photos');

-- Allow users to update their own photos
CREATE POLICY "Users can update their own photos" ON storage.objects
FOR UPDATE USING (
  bucket_id = 'cv-photos' AND auth.uid()::text = (storage.foldername(name))[1]
);

-- Allow users to delete their own photos
CREATE POLICY "Users can delete their own photos" ON storage.objects
FOR DELETE USING (
  bucket_id = 'cv-photos' AND auth.uid()::text = (storage.foldername(name))[1]
);
